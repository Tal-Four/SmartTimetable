    private void sortList() {
        String sort = "";
        if (nameSortButton.isSelected()) {
            sort = "Name";
        } else if (timeModifierSortButton.isSelected()) {
            sort = "Modifier";
        }
        String sql = "SELECT Name FROM category, user0 WHERE category.UserID = user.UserID AND user.UserID = " + User.getUserID() + " ORDER BY " + sort;

        DefaultListModel dlm = new DefaultListModel();
        
        ResultSet rs = DatabaseHandle.query(sql);
        try {
            while (rs.next()) {
                dlm.addElement(rs.getString("Name"));
            }
        } catch (SQLException ex) {
            System.err.println(ex);
        }

        this.categoryList.setModel(dlm);
    }

